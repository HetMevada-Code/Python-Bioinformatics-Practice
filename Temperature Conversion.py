# Write a program that takes a temperature in Celsius (as a floating-point number) and converts it to Fahrenheit.

# user input temp. in celcius 
Celsius = float(input("Enter temp. in Celsius= "))

# for convert Celcius to Fahrenheit, we use formula F = (C * 1.8) + 32
Fahrenheit = (Celsius * 1.8) + 32

# then print the answer in Fahrenheit
print("temp. in Fahrenheit= ", Fahrenheit)